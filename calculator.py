def sumation(a, b):
    """Sum calculation

    Args:
        a, b - numbers of operation

    Returns:
        sum of a and b
    """
    return a + b

def subtraction(a, b):
    """Subtraction calculation

    Args:
        a, b - numbers of operation

    Returns:
        subtraction of b from a
    """
    return a - b

def produce(a, b):
    """Produce calculation

    Args:
        a, b - numbers of operation

    Returns:
        produce of a with b
    """
    return a * b

def division(a, b):
    """Division calculation

    Args:
        a,b - numbers of operation

    Returns:
        division of a and b
    """
    return a / b


